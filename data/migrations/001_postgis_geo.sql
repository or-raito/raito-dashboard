-- =============================================================================
-- Step 2: Add geospatial columns to master_data (Points of Sale)
-- =============================================================================

-- Add lat/lng floats for raw geocoder output (always store the raw values)
ALTER TABLE master_data
    ADD COLUMN IF NOT EXISTS latitude         DOUBLE PRECISION,
    ADD COLUMN IF NOT EXISTS longitude        DOUBLE PRECISION,
    ADD COLUMN IF NOT EXISTS geo_confidence   VARCHAR(20),   -- 'ROOFTOP','RANGE_INTERPOLATED','GEOMETRIC_CENTER','APPROXIMATE'
    ADD COLUMN IF NOT EXISTS geo_status       VARCHAR(30),   -- 'OK','ZERO_RESULTS','PENDING','MANUAL_OVERRIDE'
    ADD COLUMN IF NOT EXISTS geo_formatted_address TEXT,     -- full address returned by Geocoding API
    ADD COLUMN IF NOT EXISTS geo_municipality VARCHAR(100),  -- parsed city/council name (EN)
    ADD COLUMN IF NOT EXISTS geo_municipality_he VARCHAR(100), -- parsed city/council name (HE)
    ADD COLUMN IF NOT EXISTS geo_geocoded_at  TIMESTAMPTZ,   -- when geocoding ran
    ADD COLUMN IF NOT EXISTS geo_point        GEOMETRY(Point, 4326); -- PostGIS point (WGS84)

-- Spatial index on the geometry column (critical for boundary queries)
CREATE INDEX IF NOT EXISTS idx_master_data_geo_point
    ON master_data USING GIST (geo_point);

-- Standard index on municipality for GROUP BY queries
CREATE INDEX IF NOT EXISTS idx_master_data_municipality
    ON master_data (geo_municipality);

-- Standard index on geo_status for filtering ungeocoded records
CREATE INDEX IF NOT EXISTS idx_master_data_geo_status
    ON master_data (geo_status);


-- =============================================================================
-- Step 3: Create municipalities_geo table to store GeoJSON boundaries
-- =============================================================================

CREATE TABLE IF NOT EXISTS municipalities_geo (
    id                SERIAL PRIMARY KEY,
    municipality_id   VARCHAR(50) UNIQUE NOT NULL,  -- geoBoundaries shapeID or CBS city code
    name_he           VARCHAR(100) NOT NULL,
    name_en           VARCHAR(100),
    region            VARCHAR(50),                  -- district (מחוז)
    geom              GEOMETRY(MultiPolygon, 4326) NOT NULL,
    population        INTEGER,
    area_km2          NUMERIC(10, 2),
    created_at        TIMESTAMPTZ DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_municipalities_geo_geom
    ON municipalities_geo USING GIST (geom);

CREATE INDEX IF NOT EXISTS idx_municipalities_geo_name_en
    ON municipalities_geo (name_en);

CREATE INDEX IF NOT EXISTS idx_municipalities_geo_name_he
    ON municipalities_geo (name_he);


-- =============================================================================
-- Step 4: Create geo_kpi_cache table (pre-aggregated for fast map rendering)
-- Regenerated by the Geo Aggregator job after each data ingestion.
-- =============================================================================

CREATE TABLE IF NOT EXISTS geo_kpi_cache (
    id                   SERIAL PRIMARY KEY,
    municipality_id      VARCHAR(50) NOT NULL REFERENCES municipalities_geo(municipality_id),
    week_label           VARCHAR(10),          -- 'W13' or NULL for all-time
    kpi_revenue_ils      NUMERIC(12, 2) DEFAULT 0,
    kpi_units_sold       INTEGER DEFAULT 0,
    kpi_pos_count        INTEGER DEFAULT 0,    -- active POS in municipality
    kpi_avg_price        NUMERIC(8, 2),
    distributor          VARCHAR(30),          -- 'all' | 'icedream' | 'mayyan' | 'biscotti'
    computed_at          TIMESTAMPTZ DEFAULT NOW(),
    UNIQUE (municipality_id, week_label, distributor)
);

CREATE INDEX IF NOT EXISTS idx_geo_kpi_municipality
    ON geo_kpi_cache (municipality_id);

CREATE INDEX IF NOT EXISTS idx_geo_kpi_week
    ON geo_kpi_cache (week_label);


-- =============================================================================
-- Step 5: Trigger to keep geo_point in sync with lat/lng
-- =============================================================================

CREATE OR REPLACE FUNCTION sync_geo_point()
RETURNS TRIGGER AS $$
BEGIN
    IF NEW.latitude IS NOT NULL AND NEW.longitude IS NOT NULL THEN
        NEW.geo_point = ST_SetSRID(ST_MakePoint(NEW.longitude, NEW.latitude), 4326);
    ELSE
        NEW.geo_point = NULL;
    END IF;
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trg_sync_geo_point ON master_data;
CREATE TRIGGER trg_sync_geo_point
    BEFORE INSERT OR UPDATE OF latitude, longitude ON master_data
    FOR EACH ROW EXECUTE FUNCTION sync_geo_point();


