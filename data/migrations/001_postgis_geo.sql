-- =============================================================================

-- View: POS with sales, joined to municipality
-- Actual schema: master_data.entity (text) = POS identifier, linked to
-- sale_points.id via entity = id::text. Distributor name from distributors.name_en.
-- sales_transactions joins via sale_point_id. Week is week_number (smallint).
CREATE OR REPLACE VIEW v_pos_geo_summary AS
SELECT
    sp.id AS sale_point_id,
    sp.branch_name_he AS pos_name,
    d.name_en AS distributor,
    md.geo_municipality,
    md.geo_municipality_he,
    md.latitude,
    md.longitude,
    md.geo_confidence,
    md.geo_status,
    COALESCE(SUM(st.revenue_ils), 0)  AS total_revenue,
    COALESCE(SUM(st.units_sold), 0)   AS total_units,
    COUNT(DISTINCT st.week_number)    AS weeks_active
FROM sale_points sp
LEFT JOIN master_data md ON md.entity = sp.id::text
LEFT JOIN distributors d ON d.id = sp.distributor_id
LEFT JOIN sales_transactions st ON st.sale_point_id = sp.id
WHERE md.geo_status = 'OK'
GROUP BY sp.id, sp.branch_name_he, d.name_en,
         md.geo_municipality, md.geo_municipality_he,
         md.latitude, md.longitude, md.geo_confidence, md.geo_status;


-- View: Municipality KPI rollup (all-time, all distributors)
CREATE OR REPLACE VIEW v_municipality_kpi AS
SELECT
    mg.municipality_id,
    mg.name_he,
    mg.name_en,
    mg.region,
    COALESCE(SUM(st.revenue_ils), 0)  AS total_revenue,
    COALESCE(SUM(st.units_sold), 0)   AS total_units,
    COUNT(DISTINCT sp.id)             AS pos_count
FROM municipalities_geo mg
LEFT JOIN master_data md
    ON md.geo_municipality = mg.name_en
    AND md.geo_status = 'OK'
LEFT JOIN sale_points sp ON sp.id::text = md.entity
LEFT JOIN sales_transactions st ON st.sale_point_id = sp.id
GROUP BY mg.municipality_id, mg.name_he, mg.name_en, mg.region;


